    <script>
        // Modal 

         function showModal(message, type) {
            const modal = document.getElementById('modal');
            const modalText = document.getElementById('modal-text');
            modalText.textContent = message;
            modal.classList.remove('hidden');
        }
        function closeModal() {
            document.getElementById('modal').classList.add('hidden');
        }

        // Toggle Forms
        function toggleForms() {
            const loginForm = document.getElementById('loginForm');
            const signupForm = document.getElementById('signupForm');

            if (loginForm.classList.contains('hidden')) {
                signupForm.classList.add('fade-enter');
                signupForm.classList.remove('fade-enter-active');
                setTimeout(() => {
                    signupForm.classList.add('hidden');
                    loginForm.classList.remove('hidden');
                    setTimeout(() => loginForm.classList.add('fade-enter-active'), 10);
                }, 300);
            } else {
                loginForm.classList.add('fade-enter');
                loginForm.classList.remove('fade-enter-active');
                setTimeout(() => {
                    loginForm.classList.add('hidden');
                    signupForm.classList.remove('hidden');
                    setTimeout(() => signupForm.classList.add('fade-enter-active'), 10);
                }, 300);
            }
        }

        // Image Slider Functionality
        const images = ["Images/Img1.jpg", "Images/Img3.jpg", "Images/Img4.jpg", "Images/Img6.jpg"];
        let index = 0;
        const sliderContainer = document.getElementById("slider-container");

        function changeImage() {
            let newImage = document.createElement("img");
            newImage.src = images[(index + 1) % images.length];
            newImage.className = "w-full h-full object-cover absolute transition-transform duration-1000 ease-in-out";
            newImage.style.transform = "translateX(100%)";

            sliderContainer.appendChild(newImage);

            setTimeout(() => {
                newImage.style.transform = "translateX(0)";
                sliderContainer.firstElementChild.style.transform = "translateX(-100%)";
            }, 50);

            setTimeout(() => {
                sliderContainer.removeChild(sliderContainer.firstElementChild);
            }, 1000);

            index = (index + 1) % images.length;
        }

        setInterval(changeImage, 4000);
    </script>
